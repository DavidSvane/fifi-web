		<p>Fifi er et firma stiftet i 2016, hvorigennem vi har udviklet en gratis app med musik, digte og eventyr.</p>
		<p>Fifi er Danmarks første og eneste app med musik og fortællinger, som henvender sig til patienter i den danske sundhedssektor. Formålet er at mindske stress og angst og at udfylde de lange ventetider.</p>
		<p>App'en indeholder nøje udvalgte musikstykker samt højtlæste danske digte og eventyr, og bliver opdateret med nyt materiale jævnligt. Drømmen er, at forbedre den enkelte patients oplevelse af at være i et hospitalsforløb.</p>
		<p>Samarbejdspartnerne omfatter indtil videre Region Hovedstaden, Region Sjælland og Region Syddanmark.</p>
